// Helpers to build a store with a known set of nodes/edges for each section
function makeNode(
  id: string,
  label: string,
  kind: GraphNode["kind"],
  metadata: Record<string, unknown> = {}
): GraphNode {
  return {
    id,
    label,
    kind,
    sourceFile: `src/${id}.ts`,
    sourceLocation: null,
    confidence: "EXTRACTED",
    confidenceScore: 1.0,
    lastVerified: Date.now(),
    queryCount: 0,
    metadata,
  };
}

function makeEdge(
  source: string,
  target: string,
  relation: GraphEdge["relation"] = "imports"
): GraphEdge {
  return {
    source,
    target,
    relation,
    confidence: "EXTRACTED",
    confidenceScore: 1.0,
    sourceFile: "x.ts",
    sourceLocation: null,
    lastVerified: Date.now(),
    metadata: {},
  };
}

describe("generateSummary — data-driven Views", () => {
  let dir: string;
  let dbPath: string;
  let store: GraphStore;

  beforeEach(async () => {
    dir = mkdtempSync(join(tmpdir(), "engram-views-"));
    dbPath = join(dir, "graph.db");
    store = await GraphStore.open(dbPath);

    // Seed the store with a richer graph that exercises every section:
    // - 15 function nodes (for god nodes — top 15 max requested by refactor view)
    // - 2 real hot files (git-miner pattern with metadata.type === "hot_file")
    // - 3 real patterns (non-hot-file)
    // - 4 decisions
    // - 3 mistakes
    // - import edges to compute deps

    const nodes: GraphNode[] = [];
    const edges: GraphEdge[] = [];

    for (let i = 0; i < 15; i++) {
      nodes.push(makeNode(`fn${i}`, `func${i}()`, "function"));
    }
    // Create import edges so god nodes get degrees
    for (let i = 0; i < 14; i++) {
      edges.push(makeEdge(`fn${i}`, `fn${i + 1}`));
    }
    // Add more edges on fn0 to make it a super-god
    for (let i = 2; i < 15; i++) edges.push(makeEdge(`fn${i}`, `fn0`));

    // Hot files (git-miner style)
    nodes.push(
      makeNode("hot_a", "🔥 src/a.ts (8 changes)", "pattern", {
        type: "hot_file",
        changeCount: 8,
      }),
      makeNode("hot_b", "🔥 src/b.ts (6 changes)", "pattern", {
        type: "hot_file",
        changeCount: 6,
      })
    );

    // Real patterns (not hot files)
    nodes.push(
      makeNode("pat1", "always use immutable state", "pattern", {}),
      makeNode("pat2", "prefer composition over inheritance", "pattern", {}),
      makeNode("pat3", "pure functions for business logic", "pattern", {})
    );

    // Decisions
    nodes.push(
      makeNode("dec1", "chose JWT for scaling", "decision", {}),
      makeNode("dec2", "picked Postgres over Mongo", "decision", {}),
      makeNode("dec3", "use Vitest not Jest", "decision", {}),
      makeNode("dec4", "go with Fastify", "decision", {})
    );

    // Mistakes
    nodes.push(
      makeNode("mis1", "fs.readFile in event loop caused prod stall", "mistake", {}),
      makeNode("mis2", "forgot to await DB connection", "mistake", {}),
      makeNode("mis3", "nested callbacks in legacy auth", "mistake", {})
    );

    store.bulkUpsert(nodes, edges);
  });

  afterEach(() => {
    store.close();
    rmSync(dir, { recursive: true, force: true });
  });

  it("default view (no argument) equals VIEWS.general", () => {
    const defaultOutput = generateSummary(store);
    const generalOutput = generateSummary(store, VIEWS.general);
    expect(defaultOutput).toBe(generalOutput);
  });

  it("all 4 named views exist and are objects", () => {
    expect(VIEWS.general).toBeDefined();
    expect(VIEWS["bug-fix"]).toBeDefined();
    expect(VIEWS.feature).toBeDefined();
    expect(VIEWS.refactor).toBeDefined();
    for (const v of Object.values(VIEWS)) {
      expect(Array.isArray(v.sections)).toBe(true);
      expect(v.sections.length).toBeGreaterThan(0);
    }
  });

  it("views produce meaningfully different outputs (not just pagination)", () => {
    const general = generateSummary(store, VIEWS.general);
    const bugFix = generateSummary(store, VIEWS["bug-fix"]);
    const feature = generateSummary(store, VIEWS.feature);
    const refactor = generateSummary(store, VIEWS.refactor);

    // Each pair should differ
    expect(bugFix).not.toBe(general);
    expect(feature).not.toBe(general);
    expect(refactor).not.toBe(general);
    expect(bugFix).not.toBe(feature);
    expect(feature).not.toBe(refactor);
  });

  it("bug-fix view has 🔥 Hot files section; general view does not", () => {
    const bugFix = generateSummary(store, VIEWS["bug-fix"]);
    const general = generateSummary(store, VIEWS.general);
    expect(bugFix).toContain("🔥 Hot files");
    expect(general).not.toContain("🔥 Hot files");
  });

  it("bug-fix view omits the Decisions section; feature view emphasizes it", () => {
    const bugFix = generateSummary(store, VIEWS["bug-fix"]);
    const feature = generateSummary(store, VIEWS.feature);
    expect(bugFix).not.toContain("## Decisions");
    expect(feature).toContain("## Decisions");
  });

  it("refactor view lists more god nodes than general view (15 vs 8)", () => {
    const general = generateSummary(store, VIEWS.general);
    const refactor = generateSummary(store, VIEWS.refactor);
    const generalFnCount = (general.match(/func\d+\(\)/g) ?? []).length;
    const refactorFnCount = (refactor.match(/func\d+\(\)/g) ?? []).length;
    expect(refactorFnCount).toBeGreaterThan(generalFnCount);
  });

  it("hot file nodes are NOT included in the Patterns section (filtered by metadata.type)", () => {
    const refactor = generateSummary(store, VIEWS.refactor);
    // Extract the Patterns section only
    const patternsMatch = refactor.match(/## Patterns\n\n([\s\S]*?)(?=\n##|\n<!-- engram:end)/);
    const patternsSection = patternsMatch?.[1] ?? "";
    // Real patterns should be there
    expect(patternsSection).toContain("immutable state");
    // Hot files should NOT appear in patterns section
    expect(patternsSection).not.toContain("🔥");
  });

  it("output always starts with the AUTOGEN_START marker and ends with AUTOGEN_END", () => {
    for (const view of Object.values(VIEWS)) {
      const out = generateSummary(store, view);
      expect(out.startsWith("<!-- engram:start -->")).toBe(true);
      expect(out.trimEnd().endsWith("<!-- engram:end -->")).toBe(true);
    }
  });

  it("empty graph + each view → renders without crashing", async () => {
    const emptyDir = mkdtempSync(join(tmpdir(), "engram-views-empty-"));
    const empty = await GraphStore.open(join(emptyDir, "graph.db"));
    try {
      for (const view of Object.values(VIEWS)) {
        expect(() => generateSummary(empty, view)).not.toThrow();
      }
    } finally {
      empty.close();
      rmSync(emptyDir, { recursive: true, force: true });
    }
  });

  it("custom view with a single section works", () => {
    const minimalView: View = {
      name: "minimal",
      sections: [{ section: "mistakes", limit: 2, heading: "Only mistakes" }],
    };
    const out = generateSummary(store, minimalView);
    expect(out).toContain("Only mistakes");
    expect(out).not.toContain("Core entities");
    expect(out).not.toContain("Decisions");
  });
});

describe("autogen() — task flag", () => {
  let tmpDir: string;

  beforeEach(async () => {
    tmpDir = mkdtempSync(join(tmpdir(), "engram-autogen-task-"));
    mkdirSync(join(tmpDir, "src"), { recursive: true });
    writeFileSync(
      join(tmpDir, "src", "index.ts"),
      `export function main() { return 42; }\n`
    );
    await init(tmpDir);
  });

  afterEach(() => {
    rmSync(tmpDir, { recursive: true, force: true });
  });

  it("unknown task throws with a descriptive error listing valid keys", async () => {
    await expect(
      autogen(tmpDir, "claude", "bogus-task-name")
    ).rejects.toThrow(/unknown task "bogus-task-name"/);
    await expect(
      autogen(tmpDir, "claude", "bogus-task-name")
    ).rejects.toThrow(/general/);
  });

  it("known task name resolves to the matching view", async () => {
    const result = await autogen(tmpDir, "claude", "bug-fix");
    expect(result.view).toBe("bug-fix");
  });

  it("undefined task defaults to general", async () => {
    const result = await autogen(tmpDir, "claude");
    expect(result.view).toBe("general");
  });
});

describe("autogen() — v3.0 dual-emit (CLAUDE.md + AGENTS.md by default)", () => {
  let tmpDir: string;

  beforeEach(async () => {
    tmpDir = mkdtempSync(join(tmpdir(), "engram-autogen-dual-"));
    mkdirSync(join(tmpDir, "src"), { recursive: true });
    writeFileSync(
      join(tmpDir, "src", "index.ts"),
      `export function main() { return 42; }\n`
    );
    await init(tmpDir);
  });

  afterEach(() => {
    rmSync(tmpDir, { recursive: true, force: true });
  });

  it("auto target (no flag) emits BOTH CLAUDE.md and AGENTS.md", async () => {
    const result = await autogen(tmpDir);
    expect(result.files).toHaveLength(2);
    expect(result.files).toEqual(
      expect.arrayContaining([
        join(tmpDir, "CLAUDE.md"),
        join(tmpDir, "AGENTS.md"),
      ])
    );
    expect(existsSync(join(tmpDir, "CLAUDE.md"))).toBe(true);
    expect(existsSync(join(tmpDir, "AGENTS.md"))).toBe(true);
  });

  it("auto target also updates legacy .cursorrules if present", async () => {
    writeFileSync(join(tmpDir, ".cursorrules"), "# legacy cursor rules\n");
    const result = await autogen(tmpDir);
    expect(result.files).toHaveLength(3);
    expect(result.files).toEqual(
      expect.arrayContaining([
        join(tmpDir, "CLAUDE.md"),
        join(tmpDir, "AGENTS.md"),
        join(tmpDir, ".cursorrules"),
      ])
    );
  });

  it("explicit --target=claude emits ONLY CLAUDE.md", async () => {
    const result = await autogen(tmpDir, "claude");
    expect(result.files).toEqual([join(tmpDir, "CLAUDE.md")]);
    expect(existsSync(join(tmpDir, "AGENTS.md"))).toBe(false);
  });

  it("explicit --target=agents emits ONLY AGENTS.md", async () => {
    const result = await autogen(tmpDir, "agents");
    expect(result.files).toEqual([join(tmpDir, "AGENTS.md")]);
    expect(existsSync(join(tmpDir, "CLAUDE.md"))).toBe(false);
  });

  it("CLAUDE.md and AGENTS.md contain identical engram-generated content", async () => {
    await autogen(tmpDir);
    const claudeContent = readFileSync(join(tmpDir, "CLAUDE.md"), "utf-8");
    const agentsContent = readFileSync(join(tmpDir, "AGENTS.md"), "utf-8");
    // Both should have the same engram block (single source of truth)
    const claudeBlock = claudeContent.match(/<!-- engram:start -->[\s\S]*<!-- engram:end -->/)?.[0];
    const agentsBlock = agentsContent.match(/<!-- engram:start -->[\s\S]*<!-- engram:end -->/)?.[0];
    expect(claudeBlock).toBeDefined();
    expect(agentsBlock).toBeDefined();
    expect(claudeBlock).toEqual(agentsBlock);
  });
});

