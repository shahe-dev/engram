const START = "<!-- engram:start -->";
const END = "<!-- engram:end -->";

const SUMMARY = `${START}\n## Codebase Structure (auto-generated by engram)\n\nfresh content\n${END}`;

describe("writeToFile marker state machine", () => {
  let dir: string;
  let filePath: string;

  beforeEach(() => {
    dir = mkdtempSync(join(tmpdir(), "engram-autogen-"));
    filePath = join(dir, "CLAUDE.md");
  });

  afterEach(() => {
    rmSync(dir, { recursive: true, force: true });
  });

  it("state=none: appends to empty file", () => {
    writeToFile(filePath, SUMMARY);
    const result = readFileSync(filePath, "utf-8");
    expect(result).toContain(START);
    expect(result).toContain(END);
    expect(result).toContain("fresh content");
    expect(result.endsWith("\n")).toBe(true);
  });

  it("state=none: appends after existing user content with blank-line separator", () => {
    writeFileSync(filePath, "# My Project\n\nUser-authored intro.\n");
    writeToFile(filePath, SUMMARY);
    const result = readFileSync(filePath, "utf-8");
    // User content preserved
    expect(result).toContain("# My Project");
    expect(result).toContain("User-authored intro.");
    // Summary appended
    expect(result).toContain("fresh content");
    // Blank line separates user content from engram section
    expect(result.indexOf("User-authored intro.\n\n<!--")).toBeGreaterThan(-1);
  });

  it("state=balanced: in-place replacement preserves content ABOVE and BELOW markers", () => {
    const initial = [
      "# My Project",
      "",
      "Header paragraph.",
      "",
      START,
      "## old engram section",
      "stale content",
      END,
      "",
      "## Manual section the user wrote",
      "",
      "Footer paragraph.",
      "",
    ].join("\n");
    writeFileSync(filePath, initial);
    writeToFile(filePath, SUMMARY);
    const result = readFileSync(filePath, "utf-8");
    // Content above markers preserved
    expect(result).toContain("# My Project");
    expect(result).toContain("Header paragraph.");
    // Content below markers preserved — this is the bug we're fixing
    expect(result).toContain("## Manual section the user wrote");
    expect(result).toContain("Footer paragraph.");
    // Old engram content gone
    expect(result).not.toContain("stale content");
    // New engram content present
    expect(result).toContain("fresh content");
    // Exactly one start and one end marker (no duplication)
    expect([...result.matchAll(new RegExp(START, "g"))].length).toBe(1);
    expect([...result.matchAll(new RegExp(END, "g"))].length).toBe(1);
  });

  it("state=unbalanced (two start markers, one end): throws descriptive error", () => {
    const initial = [
      "# Project",
      START,
      "first section",
      START, // duplicate — should trigger unbalanced
      "second section",
      END,
    ].join("\n");
    writeFileSync(filePath, initial);
    expect(() => writeToFile(filePath, SUMMARY)).toThrow(/unbalanced|start marker/i);
    // File must be unchanged (we crash before writing)
    expect(readFileSync(filePath, "utf-8")).toBe(initial);
  });

  it("state=unbalanced (start without end): throws descriptive error", () => {
    const initial = `# Project\n${START}\norphaned content with no end marker\n`;
    writeFileSync(filePath, initial);
    expect(() => writeToFile(filePath, SUMMARY)).toThrow(/marker/i);
    expect(readFileSync(filePath, "utf-8")).toBe(initial);
  });

  it("state=unbalanced (end without start): throws descriptive error", () => {
    const initial = `# Project\nsome content\n${END}\nmore content\n`;
    writeFileSync(filePath, initial);
    expect(() => writeToFile(filePath, SUMMARY)).toThrow(/marker/i);
    expect(readFileSync(filePath, "utf-8")).toBe(initial);
  });

  it("state=fence-nested: markers inside a code fence are NOT treated as real", () => {
    // User is documenting what engram markers look like inside a code block.
    // Those markers should be ignored (fence depth > 0) and the file treated
    // as having no real markers → append normally.
    const initial = [
      "# My Project",
      "",
      "engram uses these markers:",
      "",
      "```markdown",
      START,
      "...engram content...",
      END,
      "```",
      "",
      "That's the convention.",
    ].join("\n");
    writeFileSync(filePath, initial);
    writeToFile(filePath, SUMMARY);
    const result = readFileSync(filePath, "utf-8");
    // Original fenced documentation intact
    expect(result).toContain("```markdown");
    expect(result).toContain("engram uses these markers:");
    expect(result).toContain("That's the convention.");
    // The fenced markers were NOT touched — they still say "...engram content..."
    expect(result).toContain("...engram content...");
    // A new engram section was appended at the end
    expect(result).toContain("fresh content");
    // Should have 2 start markers total (one in fence, one in new appended section)
    expect([...result.matchAll(new RegExp(START, "g"))].length).toBe(2);
  });

  it("state=balanced with fenced markers coexisting: replaces only the real pair", () => {
    const initial = [
      "# Project",
      "",
      "Example of the convention:",
      "",
      "```",
      START,
      "  example inside fence",
      END,
      "```",
      "",
      "Here's the real engram section:",
      "",
      START,
      "old real content",
      END,
      "",
      "Footer.",
    ].join("\n");
    writeFileSync(filePath, initial);
    writeToFile(filePath, SUMMARY);
    const result = readFileSync(filePath, "utf-8");
    // Fenced example preserved
    expect(result).toContain("example inside fence");
    // Footer preserved
    expect(result).toContain("Footer.");
    // Real section replaced
    expect(result).not.toContain("old real content");
    expect(result).toContain("fresh content");
    // The fenced markers stayed, the real markers stayed, and the summary
    // replaced the old real content — so we still have 2 start, 2 end.
    expect([...result.matchAll(new RegExp(START, "g"))].length).toBe(2);
    expect([...result.matchAll(new RegExp(END, "g"))].length).toBe(2);
  });
});

